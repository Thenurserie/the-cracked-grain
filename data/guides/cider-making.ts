      title: "Glossary of Cider Terms",
      content: `**ABV (Alcohol by Volume)**: The percentage of alcohol in your cider. Typical cider ranges from 5-8% ABV. Calculated using original and final gravity readings.

**Acetobacter**: Bacteria that converts alcohol into acetic acid (vinegar). The enemy of cider makers. Prevented through proper sanitation and minimizing oxygen exposure.

**Acid Blend**: A mixture of acids (citric, malic, tartric) used to increase tartness in cider that tastes flat.

**Airlock**: A device that allows CO2 to escape the fermenter while preventing air and contaminants from entering. Essential for fermentation.

**Attenuation**: The percentage of sugars consumed by yeast during fermentation. High attenuation = drier cider.

**Back-Sweetening**: Adding sweetness to cider after fermentation completes, since fermentation produces dry cider by default.

**Bittersweet Apple**: Traditional cider apple variety high in tannins and sugar, low in acid. Creates full-bodied ciders.

**Bittersharp Apple**: Traditional cider apple high in both tannins and acid. Adds structure and complexity.

**Bottle Conditioning**: Natural carbonation method where yeast ferments priming sugar in sealed bottles, creating CO2.

**Campden Tablet**: Potassium or sodium metabisulfite tablet used to kill wild yeast/bacteria in fresh juice or to stabilize cider before back-sweetening.

**Carbonation**: The level of dissolved CO2 in cider, creating fizz. Measured in volumes (2.0-2.5 volumes typical for cider).

**Carboy**: A narrow-necked glass or plastic jug used for fermentation, typically 1, 3, 5, or 6 gallons.

**Cyser**: A hybrid beverage - apple juice fermented with honey. Also classified as a type of mead.

**Dry Cider**: Cider with no residual sweetness. All fermentable sugars consumed by yeast.

**Final Gravity (FG)**: Specific gravity reading after fermentation completes. Indicates remaining sugar and calculates ABV.

**Graff**: A hybrid beverage combining apple juice, malt, hops, and ale yeast. Part beer, part cider.

**Hydrometer**: Tool that measures sugar content (specific gravity) of cider before and after fermentation.

**Ice Cider**: Dessert-style cider made by concentrating apple juice through freezing before fermentation. Rich, sweet, high-alcohol.

**Lees**: Sediment of dead yeast and particulates that settles at the bottom of the fermenter.

**Must**: Unfermented juice. (Wine term also used for cider.)

**Original Gravity (OG)**: Specific gravity reading before fermentation. Indicates sugar content. Apple juice typically reads 1.045-1.055.

**Oxidation**: Exposure to oxygen that causes stale, cardboard-like off-flavors. Minimize after fermentation begins.

**Pasteurization**: Heat treatment that kills yeast and bacteria. Store-bought juice is pasteurized.

**Pectic Enzyme**: Enzyme that breaks down pectin (causes haze) to clarify cider.

**Perry**: Fermented pear juice (pear cider). Delicate and floral compared to apple cider.

**Pomace**: The solid remains (skins, seeds, pulp) after pressing apples. Used for animal feed or compost.

**Potassium Sorbate**: Yeast inhibitor that prevents fermentation restart. Used with Campden tablets to stabilize cider before back-sweetening with fermentable sugars.

**Priming Sugar**: Sugar added at bottling for bottle conditioning (natural carbonation).

**Racking**: Transferring cider from one vessel to another, leaving sediment behind. Clarifies cider and reduces off-flavors.

**Sanitization**: Killing microorganisms on equipment surfaces using chemical sanitizers. Critical for preventing contamination.

**SCOBY**: "Symbiotic Culture of Bacteria and Yeast" - used in kombucha, not cider. (Included because beginners sometimes confuse fermentation types.)

**Sharp Apple**: Traditional cider apple high in acid, low in tannins. Adds tartness.

**Specific Gravity (SG)**: Measurement of liquid density compared to water. Indicates sugar content. Water = 1.000.

**Star San**: Popular no-rinse acid sanitizer used by homebrewers and cider makers.

**Still Cider**: Non-carbonated (flat) cider. Traditional in many regions.

**Sweet Apple**: Traditional cider apple high in sugar, low in acid and tannins. Provides alcohol potential.

**Tannin**: Astringent compounds from apple skins and seeds that add structure, mouthfeel, and bitterness. Desirable in moderation.

**Wild Fermentation**: Allowing native yeast (on apple skins or in the air) to ferment cider without adding cultivated yeast. Unpredictable results.

**Xylitol**: Non-fermentable sugar alcohol used for back-sweetening. Yeast cannot ferment it, so it won't restart fermentation.

**Yeast**: Microorganisms that consume sugar and produce alcohol and CO2. The heroes of fermentation.

**Yeast Nutrient**: Supplement providing nitrogen, vitamins, and minerals that promote healthy yeast fermentation. Particularly important when adding extra sugar to boost alcohol.`
